from google.colab import drive
drive.mount('/content/drive')

import pandas as pd
import numpy as np
import os

data_path = '/content/drive/MyDrive/Cancer_Project_Data'
os.listdir(data_path)

rna = pd.read_csv(
    data_path + '/EB++AdjustPANCAN_IlluminaHiSeq_RNASeqV2.geneExp.xena.gz',
    sep='\t',
    compression='gzip'
)
rna.shape

rna = rna.set_index(rna.columns[0])
rna = rna.T
rna.reset_index(inplace=True)
rna.rename(columns={'index':'Sample_ID'}, inplace=True)
rna.head()

gene_var = rna.iloc[:,1:].var()
top_genes = gene_var.sort_values(ascending=False).head(500).index
rna_reduced = rna[['Sample_ID'] + list(top_genes)]
rna_reduced.shape

mutation = pd.read_csv(
    data_path + '/mc3.v0.2.8.PUBLIC.nonsilentGene.xena.gz',
    sep='\t',
    compression='gzip'
)
mutation.shape

mutation_counts = mutation.iloc[:,1:].sum(axis=1)
top_mut_idx = mutation_counts.sort_values(ascending=False).head(500).index
mutation_reduced = mutation.loc[top_mut_idx]
mutation_reduced.shape

mutation_reduced = mutation_reduced.set_index(mutation_reduced.columns[0])
mutation_reduced = mutation_reduced.T
mutation_reduced.reset_index(inplace=True)
mutation_reduced.rename(columns={'index':'Sample_ID'}, inplace=True)
mutation_reduced.head()

clinical = pd.read_csv(
    data_path + '/Survival_SupplementalTable_S1_20171025_xena_sp',
    sep='\t'
)
clinical.rename(columns={'sample':'Sample_ID'}, inplace=True)
clinical.head()

rna_reduced['Sample_ID'] = rna_reduced['Sample_ID'].str[:12]
mutation_reduced['Sample_ID'] = mutation_reduced['Sample_ID'].str[:12]
clinical['Sample_ID'] = clinical['Sample_ID'].str[:12]

data = pd.merge(rna_reduced, mutation_reduced, on='Sample_ID', how='inner')
data = pd.merge(data, clinical, on='Sample_ID', how='inner')
data.shape

data.to_csv(data_path + '/final_multiomics_dataset.csv', index=False)
print('Saved')

