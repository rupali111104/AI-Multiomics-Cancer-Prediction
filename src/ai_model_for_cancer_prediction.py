class EncoderModel(nn.Module):

    def __init__(self,input_dim):

        super(EncoderModel,self).__init__()

        self.encoder = nn.Sequential(

            nn.Linear(input_dim,1024),
            nn.ReLU(),

            nn.Linear(1024,512),
            nn.ReLU(),

            nn.Linear(512,256),
            nn.ReLU()

        )

        self.classifier = nn.Linear(256,2)

    def forward(self,x):

        x = self.encoder(x)
        x = self.classifier(x)

        return x

