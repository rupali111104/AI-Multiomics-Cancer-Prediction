class MultiOmicsAttentionModel(nn.Module):

    def __init__(self):
        super(MultiOmicsAttentionModel,self).__init__()

        self.rna_encoder = nn.Sequential(
            nn.Linear(500,512),
            nn.ReLU(),
            nn.Dropout(0.4),
            nn.Linear(512,128)
        )

        self.mutation_encoder = nn.Sequential(
            nn.Linear(500,128),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(128,64)
        )

        self.attention = nn.Sequential(
            nn.Linear(192,128),
            nn.Tanh(),
            nn.Linear(128,192)
        )

        self.classifier = nn.Linear(192,2)

    def forward(self,rna,mutation):

        rna_feat = self.rna_encoder(rna)
        mut_feat = self.mutation_encoder(mutation)

        combined = torch.cat((rna_feat,mut_feat),dim=1)

        attn = torch.softmax(self.attention(combined),dim=1)

        fused = combined * attn

        output = self.classifier(fused)

        return output

