from torch_geometric.nn import GCNConv
import torch.nn.functional as F

import torch

num_nodes = 192  # fused feature size

# Self-loop graph (safe fallback)
edge_index = torch.arange(num_nodes).unsqueeze(0).repeat(2,1)

class MultiOmicsGNN(nn.Module):

    def __init__(self):

        super(MultiOmicsGNN,self).__init__()

        self.rna_encoder = nn.Sequential(
            nn.Linear(500,512),
            nn.ReLU(),
            nn.Dropout(0.4),
            nn.Linear(512,128)
        )

        self.mutation_encoder = nn.Sequential(
            nn.Linear(500,128),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(128,64)
        )

        # ✅ Improved attention
        self.attention = nn.Sequential(
            nn.Linear(192,128),
            nn.Tanh(),
            nn.Linear(128,192)
        )

        self.gnn1 = GCNConv(192,128)
        self.gnn2 = GCNConv(128,64)

        self.classifier = nn.Linear(64,2)

    def forward(self,rna,mutation):

        rna_feat = self.rna_encoder(rna)
        mut_feat = self.mutation_encoder(mutation)

        combined = torch.cat((rna_feat,mut_feat),dim=1)

        attn = torch.softmax(self.attention(combined),dim=1)

        fused = combined * attn

        gnn_out = F.relu(self.gnn1(fused, edge_index))
        gnn_out = F.relu(self.gnn2(gnn_out, edge_index))

        output = self.classifier(gnn_out)

        return output

